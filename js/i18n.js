function detectarIdiomaNavegador() {
    try {
        const candidatos = Array.isArray(navigator.languages) && navigator.languages.length
            ? navigator.languages
            : [navigator.language || navigator.userLanguage || ""];

        for (const valor of candidatos) {
            const normalizado = String(valor || "").trim().toLowerCase();
            if (normalizado.startsWith("es")) {
                return "es";
            }
        }
    } catch (error) {
        console.warn("No se pudo detectar el idioma del navegador:", error);
    }

    return "en";
}

function getCurrentLang() {
    const saved = localStorage.getItem(LANG_STORAGE_KEY);
    if (saved && I18N[saved]) {
        return saved;
    }
    return detectarIdiomaNavegador();
}

function setCurrentLang(lang) {
    const finalLang = I18N[lang] ? lang : "en";
    localStorage.setItem(LANG_STORAGE_KEY, finalLang);
    applyTranslations();
    document.dispatchEvent(new CustomEvent("languageChanged", { detail: { lang: finalLang } }));
}

function t(key, params = {}) {
    const lang = getCurrentLang();
    let value = I18N?.[lang]?.[key] ?? I18N?.en?.[key] ?? key;

    if (typeof value !== "string") {
        return value;
    }

    return value.replace(/\{(\w+)\}/g, (_, variable) => {
        return params[variable] !== undefined ? String(params[variable]) : `{${variable}}`;
    });
}

function applyTranslations(root = document) {
    if (!root) return;

    root.querySelectorAll("[data-i18n]").forEach(el => {
        const key = el.dataset.i18n;
        el.textContent = t(key);
    });

    root.querySelectorAll("[data-i18n-html]").forEach(el => {
        const key = el.dataset.i18nHtml;
        el.innerHTML = t(key);
    });

    root.querySelectorAll("[data-i18n-placeholder]").forEach(el => {
        const key = el.dataset.i18nPlaceholder;
        el.setAttribute("placeholder", t(key));
    });

    root.querySelectorAll("[data-i18n-title]").forEach(el => {
        const key = el.dataset.i18nTitle;
        el.setAttribute("title", t(key));
    });

    root.querySelectorAll("[data-i18n-aria-label]").forEach(el => {
        const key = el.dataset.i18nAriaLabel;
        el.setAttribute("aria-label", t(key));
    });
}

document.addEventListener("DOMContentLoaded", () => {
    applyTranslations();
});
